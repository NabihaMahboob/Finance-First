    <h1>🛠️ Admin Dashboard Overview</h1>

    <p>The Admin Dashboard is the central hub for managing all core aspects of the Finance First website. Below is an overview of each key feature:</p>

    <ol>
      <li><strong>📊 Dashboard (Monitoring Page):</strong>  
        <p>This page displays the current status of all major website features, such as the database connection, login system, appointment services, and more. Each feature is marked as <span style="color:green;">Online</span> or <span style="color:red;">Offline</span> so admins can quickly identify any problems.</p>
      </li>

      <li><strong>📨 Requests:</strong>
        <p>Displays all user-submitted messages from the Contact form. Admins can review inquiries or support tickets submitted by users, helping provide customer support and handle feedback.</p>
      </li>

      <li><strong>👥 Manage Users:</strong>
        <p>Allows administrators to:
          <ul>
            <li>Add new users manually</li>
            <li>Edit user information (e.g. name, email, role)</li>
            <li>Disable or permanently delete accounts</li>
          </ul>
        </p>
      </li>

      <li><strong>💬 Edit Forum:</strong>
        <p>This section allows admins to:
          <ul>
            <li>Add new forum discussion posts</li>
            <li>Delete inappropriate or outdated forum entries</li>
          </ul>
        </p>
      </li>

      <li><strong>🛒 Edit Shop:</strong>
        <p>Admins can manage all store-related content including:
          <ul>
            <li>Adding new products or services</li>
            <li>Editing existing product details such as price, image, or description</li>
            <li>Removing outdated or sold-out items</li>
          </ul>
        </p>
      </li>
    </ol>

    <p>This admin panel ensures that website content, services, and users can be efficiently managed and maintained.</p>
