    <h1>📊 How to Use the Budgeting Tools</h1>

    <p>Track your income and expenses to manage your personal finances more effectively. The budgeting tool breaks your entries into categories to help visualize your financial health.</p>

    <h2>📝 Add Income or Expenses</h2>
    <ol>
      <li><strong>Log in to your account:</strong> Use your credentials to access the user dashboard.</li>
      <li><strong>Navigate to the "Budgeting" Page:</strong> Click on the “Budget” link from the menu.</li>
      <li><strong>Choose Type:</strong> Select either <em>“Income”</em> or <em>“Expense”</em> using the radio button or dropdown.</li>
      <li><strong>Enter the details:</strong>
        <ul>
          <li>💼 <strong>Category</strong> (e.g., Salary, Food, Rent, Transport)</li>
          <li>💵 <strong>Amount</strong> (Enter in numeric format like 1500.00)</li>
          <li>📝 <strong>Optional Notes</strong> (e.g., “July rent”)</li>
        </ul>
      </li>
      <li><strong>Click “Add” or “Submit”</strong> to save the entry into your personal database.</li>
    </ol>

    <h2>📈 View Budget Summary</h2>
    <ul>
      <li>Visit the “Budget Overview” section</li>
      <li>You’ll see:
        <ul>
          <li>Pie chart or bar graph of income vs. expenses (Chart.js)</li>
          <li>Total earnings</li>
          <li>Total spending</li>
          <li>Balance = Income − Expense</li>
        </ul>
      </li>
    </ul>

    <p><strong>Tips:</strong></p>
    <ul>
      <li>Log expenses daily for accuracy</li>
      <li>Use descriptive categories so your graphs are meaningful</li>
      <li>Check your balance weekly to stay on track</li>
    </ul>

