    <h1>💬 How to Use the Community Forum</h1>

    <p>Our forums allow you to share financial knowledge, ask questions, and learn from other users.</p>

    <ol>
      <li><strong>Log in to your account:</strong> Use your email and password on the Login page to access your dashboard.</li>
      <li><strong>Navigate to the Forums:</strong> In the main menu, click on “Forums” to open the discussion area.</li>
      <li><strong>Browse Topics:</strong> You will see a list of recent forum posts. Each post includes:
        <ul>
          <li>Title</li>
          <li>Description</li>
          <li>User who posted</li>
          <li>Date posted</li>
        </ul>
      </li>
      <li><strong>Read a post:</strong> All forum posts appear on the Forum page</li>
      <li><strong>Post a new topic:</strong> Use the “Add New Post” button to start a discussion or ask a question.
      You must be logged in for this</li>
        <ul>
          <li>Click on “Add New Post”</li>
          <li>Fill in:
            <ul>
              <li><strong>Title:</strong> Choose something short and descriptive</li>
              <li><strong>Description:</strong> Provide detailed content, question, or tip</li>
            </ul>
          </li>
          <li>Click “Submit” to post it to the forum</li>
        </ul>
      </li>
    </ol>

    <p><strong>Forum Rules:</strong></p>
    <ul>
      <li>Be respectful and polite to other users</li>
      <li>No spam or advertisements</li>
      <li>Stay on topic: Personal finance and investing only</li>
    </ul>

