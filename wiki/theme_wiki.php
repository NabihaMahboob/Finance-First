    <h1>🎨 How to Switch Between Website Themes</h1>

    <p>This website supports multiple visual themes (Default, Dark Mode, Pink). You can personalize your experience by switching between them anytime.</p>

    <h2>🔁 Steps to Switch Themes</h2>
    <ol>
      <li><strong>Locate the Theme Switcher:</strong>
        <ul>
          <li>All Home/Dashboard pages have a dropdown at the top which allows you to select a theme.</li>
        </ul>
      </li>

      <li><strong>Choose a Theme:</strong>
        <ul>
          <li>Options typically include:
            <ul>
              <li>🌞 Default (Bright)</li>
              <li>🌙 Dark Mode</li>
              <li>🩷 Pink Theme</li>
            </ul>
          </li>
        </ul>
      </li>

      <li><strong>Apply the Theme:</strong>
        <ul>
          <li>Click the desired theme. The site will instantly reload with the new design and color scheme applied.</li>
        </ul>
      </li>
    </ol>

    <p><strong>Note:</strong> Themes apply to the entire website and enhance accessibility.</p>

    <p>If the theme doesn’t change:</p>
    <ul>
      <li>Make sure JavaScript is enabled in your browser</li>
      <li>Try refreshing the page</li>
    </ul>

